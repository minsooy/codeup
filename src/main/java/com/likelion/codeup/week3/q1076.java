package com.likelion.codeup.week3;

import java.util.Scanner;

public class q1076 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        char end = sc.next().charAt(0);

        for (char i = 'a'; i <= end ; i++) {
            System.out.println(i);
        }
    }
}
